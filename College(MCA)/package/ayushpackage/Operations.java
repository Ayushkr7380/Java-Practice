// Define the package name
package ayushpackage;

// Define a public class named Operations
public class Operations {

    // Method to perform addition of two integers
    public int addition(int x1, int x2) {
        return x1 + x2;  // Returns the sum
    }

    // Method to perform multiplication of two integers
    public int multiplication(int x1, int x2) {
        return x1 * x2;  // Returns the product
    }

    // Method to perform subtraction of two integers
    public int subtraction(int x1, int x2) {
        return x1 - x2;  // Returns the difference
    }

    // Method to perform division of two integers
    public int division(int x1, int x2) {
        return x1 / x2;  // Returns the quotient (note: integer division)
    }

    // Method to check if x1 is greater than x2
    public boolean isGreater(int x1, int x2) {
        return x1 > x2;
    }

    // Method to check if x1 is less than x2
    public boolean isLess(int x1, int x2) {
        return x1 < x2;
    }

    // Method to check if x1 is equal to x2
    public boolean isEqual(int x1, int x2) {
        return x1 == x2;
    }

    // Method to check if x1 is not equal to x2
    public boolean isNotEqual(int x1, int x2) {
        return x1 != x2;
    }

    // Method to check if x1 is greater than or equal to x2
    public boolean isGreaterOrEqual(int x1, int x2) {
        return x1 >= x2;
    }

    // Method to check if x1 is less than or equal to x2
    public boolean isLessOrEqual(int x1, int x2) {
        return x1 <= x2;
    }

}
